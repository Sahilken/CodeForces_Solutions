int gcd(int a, int b) {
    if (b == 0)
        return a;
    return gcd(b, a % b);
}

int lcm(int a, int b) {
    return (a / gcd(a, b)) * b;
}

int solve() {
    int a , b , m;
    cin>>a>>b>>m;
    int lcm_ab = lcm(a, b);
    int total_fireworks = (m / a) + (m / b) + 1;
    int visible_fireworks = max((m / a) + ((m % a) >= (lcm_ab - b)), (m / b) + ((m % b) >= (lcm_ab - a)));
    return max(total_fireworks, visible_fireworks) + (int)1;
}

